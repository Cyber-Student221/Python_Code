# Variable = a container for as value. Behaves as the value it contains

#Strings
first_name = "Jay"
last_name = "Bird"
full_name = first_name + " " + last_name
print("Hello " + full_name)
#print(type(full_name))
print("\n")


#Integers
age = 21
age += 1
print("Your age is: " + str(age))
#print(type(age))
print("\n")

#Float
height = 250.5
print("Your height is: " + str(height) + "cm")
#print(type(height))
print("\n")

#Boolean
human = True
print("Are you a human: " + str(human))
#print(type(human))
print("\n")